# area calculator module
# area of circle
def areaOfCircle(radius):
    area = 3.14 * radius * radius
    return area

# area of triangle
def areaOfTriangle(base, height):
    area = 0.5 * base * height
    return area

# area of rectangle
def areaOfReactangle(length, breadth):
    area = length * breadth
    return area

# area of square
def areaOfSquare(side):
    area = side * side
    return area