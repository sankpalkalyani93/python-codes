#understanding escape sequencing in python
string1 = "I'm a trainer"
print(string1)

string2 = 'I\'m a "trainer"' # escaping single quote
print(string2)

string3 = "I'am a \"trainer\"" #escaping double quotes
print(string3)

string4 = "C:\\kalyani\\python\\" #escaping backslashes
print(string4)

string5 = "kalyani\tsankpal" #tabspace
print(string5)

string6 = "shivam\tpark\tappts\n\tgajanan-colony"
print("\n address \n", string6)